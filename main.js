let taskInp = document.getElementById('taskInp')
let taskBtn = document.getElementById('taskBtn')
let listTasks = document.getElementById('listTasks')

taskBtn.onclick = () => {
    let text = taskInp.value.trim();

    if(text){
        let li = document.createElement('li')
        let p = document.createElement('p')
        let deleteBtn = document.createElement('button')
        let editBtn = document.createElement('button'); 


        p.textContent = text;
        deleteBtn.textContent = "Удалить"
        editBtn.textContent = "Изменить"; 
        li.appendChild(p);
        li.appendChild(editBtn);   
        li.appendChild(deleteBtn); 
        listTasks.appendChild(li);

    
    deleteBtn.addEventListener("click", () => {
         li.remove();
    });


    editBtn.addEventListener("click", () => {
        const newText = prompt("Введите новое значение", p.textContent);
        if (newText !== null) {
            const trimmed = newText.trim();
            if (trimmed) p.textContent = trimmed;
     }
    });

        
}
};





































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































